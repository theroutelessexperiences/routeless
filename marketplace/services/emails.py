import logging
from django.core.mail import EmailMultiAlternatives
from django.template.loader import render_to_string
from django.utils.html import strip_tags
from django.conf import settings

logger = logging.getLogger(__name__)


# -------------------------------------------------------------------
# Sender resolution
# -------------------------------------------------------------------
def get_sender_email(purpose):
    """
    Return the sender address for a given email purpose.

    Looks up ``settings.ROUTELESS_EMAILS`` and falls back to
    ``settings.DEFAULT_FROM_EMAIL`` for unknown purposes.

    Valid purposes: marketing, booking, admin, general, partners,
    support, ankit, harshit.
    """
    emails_map = getattr(settings, "ROUTELESS_EMAILS", {})
    return emails_map.get(purpose, getattr(settings, "DEFAULT_FROM_EMAIL", "hello@therouteless.com"))


# -------------------------------------------------------------------
# Centralized email helper
# -------------------------------------------------------------------
def send_routeless_email(purpose, subject, to, template_name=None, context=None,
                         plain_body=None, reply_to=None):
    """
    Send an email using the correct sender address for *purpose*.

    Parameters
    ----------
    purpose : str
        One of the keys in ``settings.ROUTELESS_EMAILS``
        (e.g. "booking", "support", "marketing").
    subject : str
        Email subject line.
    to : list[str]
        Recipient email addresses.
    template_name : str, optional
        Path to an HTML template.  If provided, the template is rendered
        and attached as the HTML alternative.
    context : dict, optional
        Context dict passed to the template.
    plain_body : str, optional
        Plain-text body.  If *template_name* is given and *plain_body* is
        not, a plain-text version is auto-generated by stripping HTML.
    reply_to : list[str], optional
        Reply-To addresses.  Defaults to the sender address.
    """
    from_email = get_sender_email(purpose)

    if reply_to is None:
        reply_to = [from_email]

    context = context or {}
    if "site_url" not in context:
        context["site_url"] = getattr(settings, "SITE_URL", "http://127.0.0.1:8080")

    try:
        if template_name:
            html_content = render_to_string(template_name, context)
            text_content = plain_body or strip_tags(html_content)
        else:
            html_content = None
            text_content = plain_body or ""

        email = EmailMultiAlternatives(
            subject=subject,
            body=text_content,
            from_email=from_email,
            to=to,
            reply_to=reply_to,
        )

        if html_content:
            email.attach_alternative(html_content, "text/html")

        email.send(fail_silently=True)

    except Exception as e:
        logger.error(
            "Failed to send email (purpose=%s, template=%s, to=%s): %s",
            purpose, template_name, to, e,
        )


# -------------------------------------------------------------------
# Legacy helper kept for internal use — now delegates to
# send_routeless_email() with an explicit purpose.
# -------------------------------------------------------------------
def _send_html_email_safe(subject, template_name, context, recipient_list,
                          purpose="general", reply_to=None):
    """
    Backward-compatible wrapper.  New code should prefer
    ``send_routeless_email()`` directly.
    """
    send_routeless_email(
        purpose=purpose,
        subject=subject,
        to=recipient_list,
        template_name=template_name,
        context=context,
        reply_to=reply_to,
    )


