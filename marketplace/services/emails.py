# Support / account emails  →  purpose = "support"
#                              →  support@therouteless.com
# -------------------------------------------------------------------
def send_otp_email(user, otp_code):
    """
    Send OTP code for email login.
    """
    subject = "Your Routeless Login OTP"
    message = (
        f"Hello {user.username},\n\n"
        f"Your login code is: {otp_code}\n\n"
        "This code will expire in 5 minutes.\n"
        "If you did not request this, please ignore."
    )
    send_routeless_email(
        purpose="support",
        subject=subject,
        to=[user.email],
        plain_body=message,
    )


